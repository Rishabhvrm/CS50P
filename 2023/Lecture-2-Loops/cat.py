# making this file
#  > to demonstrate the use of '_' (underscore) in a loop
#  > to show '*' operator in print statement

for _ in range(3):
    print("meow")

# when required to go through a loop and we don't use the variable
# it's a convention to use _ (underscore) as that variable instead of naming it 'i' or somethin else

# same as above
print("meow\n" * 3, end = "")